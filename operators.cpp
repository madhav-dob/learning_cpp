#include <iostream>
using namespace std;

int main() {

    int x =25;
    int y = 5;

    int m = x+y;
    int n = x-y;
    int o = x/y;
    int p = ++x;
    int q = --y;
    int r = x % y;

    //printing the results :
    cout << "m " << m << "\n" ;
    cout << "n " << n << "\n" ;
    cout << "o " << o << "\n" ;
    cout << "p " << p << "\n" ;
    cout << "q " << q << "\n" ;
    cout << "r " << r << "\n" ;

    return 0;

    



}